// ! search
const form = document.getElementById('ctn-search');
const input = document.getElementById('input-search');
const resultsContainer = document.getElementById('results');
const modal = document.getElementById('myModal');
const closeModalButton = document.getElementById('closeModal');

form.addEventListener('submit', function (e) {
  e.preventDefault();

  const searchTerm = input.value.toLowerCase();
  const filteredProducts = productosHombre.filter(producto => {
    const productName = producto.nombre.toLowerCase();
    return productName.includes(searchTerm);
  });

  displayResults(filteredProducts);
  openModal();
});

closeModalButton.addEventListener('click', closeAndClearModal);

function displayResults(products) {
  resultsContainer.innerHTML = '';

  if (products.length === 0) {
    resultsContainer.textContent = 'No se encontraron productos.';
  } else {
    products.forEach(producto => {
      const productCard = document.createElement('div');
      productCard.classList.add('product-card');

      const productImage = document.createElement('img');
      productImage.src = producto.imagen;

      const productName = document.createElement('h2');
      productName.textContent = producto.nombre;

      const productPrice = document.createElement('p');
      productPrice.textContent = `Precio: ${producto.precio}`;

      productCard.appendChild(productImage);
      productCard.appendChild(productName);
      productCard.appendChild(productPrice);

      resultsContainer.appendChild(productCard);
    });
  }
}

function openModal() {
  modal.style.display = 'block';
}

function closeAndClearModal() {
  modal.style.display = 'none';
  resultsContainer.innerHTML = '';
}
